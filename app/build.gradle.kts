// Release signing lives in a gitignored keystore.properties (never the keystore
// itself, never credentials, in version control). Absent locally, release
// builds fall back to unsigned output rather than failing the build.
val keystoreProperties = Properties().apply {
    val file = rootProject.file("keystore.properties")
    if (file.exists()) file.inputStream().use { load(it) }
}

android {
    namespace = "com.linguabridge.app"
    compileSdk = 35

    defaultConfig {
        applicationId = "com.linguabridge.app"
        minSdk = 29
        targetSdk = 35
        versionCode = 1
        versionName = "1.0.0"
    }

    signingConfigs {
        if (keystoreProperties.containsKey("storeFile")) {
            create("release") {
                storeFile = file(keystoreProperties.getProperty("storeFile"))
                storePassword = keystoreProperties.getProperty("storePassword")
                keyAlias = keystoreProperties.getProperty("keyAlias")
                keyPassword = keystoreProperties.getProperty("keyPassword")
            }
        }
    }

    buildTypes {
        release {
            isMinifyEnabled = true
            isShrinkResources = true
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
            signingConfigs.findByName("release")?.let { signingConfig = it }
        }
    }

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions {
        jvmTarget = "17"
    }

    buildFeatures {
        compose = true
    }

    // Generates res/xml/locales_config.xml + the manifest's localeConfig attribute
    // from the app's res/ directories (AGP 8.1+), backing the in-app RU/EN switch
    // together with AppCompatDelegate.setApplicationLocales.
    androidResources {
        generateLocaleConfig = true
    }
}

